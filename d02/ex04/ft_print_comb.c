void	ft_print_comb(void)
{
	int	 a;
	int	 b;
	int	 c;
	int  sum;
	
	a = '0';
	b = '0';
	c = '0';
	while (a <= '7')
	{
		b = a + '1';
		while (b <= '8')
		{
			c = b + '1';
			while (c <= '9')
			{
				sum = a + b + c;
				ft_putchar(sum);
				c++;
			}
			b++;
		}
		a++;
	}
}

