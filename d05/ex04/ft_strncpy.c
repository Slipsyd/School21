char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;
	int check;

	i = 0;
	while (n >= 1)
	{
		dest[i] = src[i];
		i++;
		n--;
	}
	if (n < i)
		dest[i] = '\0';
	return (dest);
}
