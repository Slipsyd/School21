char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != dest[i])
	{
		dest[i] = src[i];
		i++;
	}
	if (dest[i] == '\0')
		return (dest);
	return (0);
}
